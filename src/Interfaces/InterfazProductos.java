    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String Nombre = nombre.getText();
            String precioactual = precio.getText();
            Productos p1 = new Productos(Nombre, Float.parseFloat(precio.getText()));
            
            if (!Nombre.isEmpty() && !precioactual.isEmpty() && isNumeric(precioactual)) {
                rp1.Crear(p1);
                limpiar();
                JOptionPane.showMessageDialog(null, "Producto creado!");
            } else {
                JOptionPane.showMessageDialog(null, "No se han llenado los campos correspondientes.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Formato Incorrecto.");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

