    /**
     * Inserta los valores en la tabla consultado los productos
     */
    public void TablaProductos() {
        DefaultTableModel tm = (DefaultTableModel) tablaproductos.getModel();
        tm.getDataVector().removeAllElements();

        List<Productos> producto = rp1.consultarTodos();
        try {


            for (Productos productos : producto) {
                Object row[] = {productos.getId(), productos.getNombre(), productos.getPrecioactual()};
                tm.addRow(row);

            }
        } catch (IllegalStateException ex) {
            JOptionPane.showMessageDialog(null, "No hay productos registrados.");

        }

    }
 
    /**
     * Compruueba si un valor String es numero o no
     *
     * @param str
     * @return
     */

     public static boolean isNumeric(String str) {
        for (char c : str.toCharArray()) {
            if (!Character.isDigit(c)) {
                return false;
            }
        }
        return true;
    }
     /**
     * Nos permite encontrar valores en la tabla sin especificar un valor exacto
     */
    public void busqueda() {
        String busqueda = busquedaproducto.getText();
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(tablaproductos.getModel());
        tablaproductos.setRowSorter(rowSorter);
        //Se llama al metodo addDocumentListener con todos sus medoso abstractos que nos permiten leer el contenido del texto y compararlo con la tabla
        busquedaproducto.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent de) {
                String text = busquedaproducto.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void removeUpdate(DocumentEvent de) {
                String text = busquedaproducto.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent de) {
                throw new UnsupportedOperationException("Not supported yet.");
            }
        });

    }

